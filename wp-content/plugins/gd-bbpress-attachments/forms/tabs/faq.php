<h4>Where can I configure the plugin?</h4>
<p>Open the Forums menu, and you will see Attachments item there. This will open a panel with global plugin settings.</p>

<h4>What are the common problems that can prevent upload to work?</h4>
<p>In some cases, it can happen that jQuery is not included in the page, even so the bbPress requires it to be loaded. That can happen if something else is unloading jQuery. If the jQuery is not present, upload will not work.
<br/>Other common issue is that WordPress Media Library upload is not working. If that is not set up, attachments upload can't work.</p>

<h4>Why is Media Library required?</h4>
<p>All attachments uploads are handled by the WordPress Media Library, and plugin uses native WordPress upload functions. When file is uploaded it will be available through Media Library. Consult WordPress documentation about Media Library requirements.</p>

<h4>Will this plugin work with standalone bbPress instalation?</h4>
<p>No. This plugin requires the plugin versions of bbPress 2.3 or higher.</p>

<h4>Does this plugin work with bbPress that is part of BuddyPress plugin?</h4>
<p>No. Plugin requires bbPress 2.3 or higher plugin.</p>

<h4>Does this plugin work with bbPress 2.3 used as site wide forums for BuddyPress plugin?</h4>
<p>Yes. But, make sure to enable 'Always Include' option for JavaScript and CSS.</p>
